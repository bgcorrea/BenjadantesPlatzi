text = 'Ella sabe Python'
print(text[0])

#Two ways to find the last letter

size_text = len(text)
print(text[size_text - 1])

print('*' * 15)

print(text[-1])

# Slicing

print(text[0:5])
print(text[:10]) # from the beginning to letter 9
print(text[5:]) # letter 4 to the end
print(text[::2]) # two letter of separation

